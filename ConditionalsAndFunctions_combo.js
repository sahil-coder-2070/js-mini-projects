// Grade calculator
function getGrade(score) {
  if (score >= 90) {
    return "A";
  } else if (score >= 80) {
    return "B";
  } else if (score >= 70) {
    return "C";
  } else {
    return "Fail";
  }
}

console.log(getGrade(85));  // Output: B

